    private void btnKullaniciEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKullaniciEkleActionPerformed
        if (!KontrolEt()) {
            JOptionPane.showMessageDialog(null, "Please fill in the blanks !");
        }
        else{
            int reply = JOptionPane.showConfirmDialog(null, "Would like to be registered according to the information you filled ?","Register ? ?", JOptionPane.YES_NO_OPTION);
            if(InfoKullanici.isIDExist(tfEklenecekID.getText())){
                JOptionPane.showMessageDialog(null, "Existing user with this id");
            }
            else{
                if(reply == JOptionPane.YES_NO_OPTION){
                    try {
                        InfoKullanici.Kullanici_Ekle(tfEklenecekAd.getText(), tfEklenecekSoyAd.getText(), jcEKLENENBOLUMLER.getSelectedItem().toString(), tfEklenecekID.getText(), tfEklenecekSIFRE.getText());
                        JOptionPane.showMessageDialog(null, "WELCOME TO AYBU LIBRARY");
                        old.setVisible(true);
                        this.dispose();
                    } catch (IOException ex) {
                        Logger.getLogger(Kay覺tOl.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
    }//GEN-LAST:event_btnKullaniciEkleActionPerformed

